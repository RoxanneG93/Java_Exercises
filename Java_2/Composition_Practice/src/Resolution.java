
public class Resolution {

//	class specific variables
	private int width;
	private int height;
	
//	constructor
	public Resolution(int width, int height) {
		this.width = width;
		this.height = height;
	}

// Getters
	public int getWidth() {
		return width;
	}


	public int getHeight() {
		return height;
	}
	
	
	
	
}
